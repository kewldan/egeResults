from __future__ import annotations

import re
from datetime import datetime, timezone

_PUNCT_RE = re.compile(r"[^\w\s]")

# Канонический ключ предмета → его альтернативные формулировки. Сайт называет один
# и тот же предмет по-разному между кампаниями/волнами («Информатика и ИКТ» →
# «Информатика», смена порядка слов и т. п.); сводим к одному ключу, иначе diff
# посчитает это новым результатом и пришлёт ложное уведомление.
#
# Важно: и ключи, и альтернативы заданы уже в «очищенном» виде (нижний регистр,
# без пунктуации, одиночные пробелы) — такими их видит поиск ПОСЛЕ очистки. Сам
# канонический ключ обязан быть неподвижной точкой ``normalize_subject`` (повторная
# нормализация ничего не меняет), поэтому канонические значения не должны встречаться
# среди альтернатив — это проверяет ``test_utils``. Неоднозначные формулировки сюда
# не вносим: одинокая «математика» не входит ни в базовую, ни в профильную.
_SUBJECT_ALIASES: dict[str, tuple[str, ...]] = {
    "русский язык": ("русский",),
    "математика профильная": (
        "математика профильный уровень",
        "математика профильного уровня",
        "профильная математика",
        "математика профиль",
    ),
    "математика базовая": (
        "математика базовый уровень",
        "математика базового уровня",
        "базовая математика",
        "математика база",
    ),
    "обществознание": ("общество",),
    "информатика": (
        "информатика кегэ",
        "информатика и икт",
        "информатика и информационно коммуникационные технологии",
        "информатика и информационно коммуникационные технологии икт",
    ),
    "английский язык": ("английский", "иностранный язык английский"),
    "немецкий язык": ("немецкий", "иностранный язык немецкий"),
    "французский язык": ("французский", "иностранный язык французский"),
    "испанский язык": ("испанский", "иностранный язык испанский"),
    "китайский язык": ("китайский", "иностранный язык китайский"),
    # Физика, химия, биология, география, история, литература — без альтернатив:
    # сайт называет их одним словом, достаточно очистки.
}

# Обратный индекс «альтернатива → канонический ключ» (строится один раз при импорте).
_CANONICAL_BY_ALIAS: dict[str, str] = {
    alias: canonical
    for canonical, aliases in _SUBJECT_ALIASES.items()
    for alias in aliases
}


def utcnow() -> datetime:
    """Текущее время в UTC (timezone-aware)."""
    return datetime.now(timezone.utc)


def normalize_subject(title: str) -> str:
    """Стабильный ключ предмета для сопоставления между проверками.

    Два шага: (1) очистка — пунктуация отбрасывается, регистр и пробелы
    нормализуются, чтобы «Математика (профильная)» и «Математика профильная» дали
    один ключ; (2) канонизация по словарю ``_SUBJECT_ALIASES`` — синонимичные
    формулировки («Информатика и ИКТ» / «Информатика») сводятся к одному предмету.
    Функция идемпотентна: канонический ключ — неподвижная точка.

    Живёт здесь (а не в провайдере), потому что ключ сопоставления — общий для
    всех источников: слой ``services.diff`` нормализует обе стороны сам и не
    зависит от того, нормализует ли конкретный провайдер ``subject``.
    """
    cleaned = _PUNCT_RE.sub(" ", title)  # убираем скобки/точки/дефисы и т. п.
    cleaned = " ".join(cleaned.split()).lower()
    return _CANONICAL_BY_ALIAS.get(cleaned, cleaned)
